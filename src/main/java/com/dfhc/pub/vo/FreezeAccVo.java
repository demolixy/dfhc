package com.dfhc.pub.vo;

import java.math.BigDecimal;

/**
 * 账户冻结参数vo/提货单完成操作vo
 * @author dfhc
 *
 */
public class FreezeAccVo {
	
	/**
	 * 业务类型
	 */
	private String businessType;
	
	/**
	 * 付款人id
	 */
	private String  payerId;
	
	/**
	 * 单据来源id
	 */
	private  String billsId;
	
	/**
	 * 变动金额
	 */
	private BigDecimal changeAmt;
	
	/**
	 * 备注 
	 */
	private String remark;
	
	/**
	 * 操作人的当前ip
	 */
	private String ip;
	
	/**
	 * 操作人id
	 */
	private String operaterId;
	
	/**
	 * 操作人名称
	 */
	private String operaterName;

	public String getPayerId() {
		return payerId;
	}

	public void setPayerId(String payerId) {
		this.payerId = payerId;
	}

	public String getBillsId() {
		return billsId;
	}

	public void setBillsId(String billsId) {
		this.billsId = billsId;
	}

	public BigDecimal getChangeAmt() {
		return changeAmt;
	}

	public void setChangeAmt(BigDecimal changeAmt) {
		this.changeAmt = changeAmt;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public String getIp() {
		return ip;
	}

	public void setIp(String ip) {
		this.ip = ip;
	}

	public String getOperaterId() {
		return operaterId;
	}

	public void setOperaterId(String operaterId) {
		this.operaterId = operaterId;
	}

	public String getOperaterName() {
		return operaterName;
	}

	public void setOperaterName(String operaterName) {
		this.operaterName = operaterName;
	}


	
	/**
	 * 账户变动业务类型
	 * @return
	 */
	public String getBusinessType() {
		return businessType;
	}

    /**
     * 设置账户变动业务类型
     * @param businessType
     */
	public void setBusinessType(String businessType) {
		this.businessType = businessType;
	}

	public FreezeAccVo() {
		super();
	}

	/**
	 * 
	 * @param businessType:账户变动业务类型
	 * @param payerId：付款方id
	 * @param billsId：业务来源id
	 * @param changeAmt：业务变化金额
	 * @param remark： 备注信息
	 * @param ip：操作人ip
	 * @param operaterId：操作人id
	 * @param operaterName：操作人名称
	 */
	public FreezeAccVo(String businessType, String payerId, String billsId,
			BigDecimal changeAmt, String remark, String ip, String operaterId,
			String operaterName) {
		super();
		this.businessType = businessType;
		this.payerId = payerId;
		this.billsId = billsId;
		this.changeAmt = changeAmt;
		this.remark = remark;
		this.ip = ip;
		this.operaterId = operaterId;
		this.operaterName = operaterName;
	}
	
	
	
	
	
	

}
