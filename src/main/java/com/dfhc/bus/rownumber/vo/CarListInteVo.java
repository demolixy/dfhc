package com.dfhc.bus.rownumber.vo;

import org.quickbundle.base.vo.RmValueObject;

public class CarListInteVo extends RmValueObject{

	private static final long serialVersionUID = 1L;

	/**
	 * 提货单号
	 */
	private String billid;
	/**
	 * 车牌号
	 */
	private String truckNumber;
	/**
	 * 排队号
	 */
	private String queueid;
	/**
	 * 该产品当前场内车数、最大容量（5/15）
	 */
	private String truckCounts;
	/**
	 * 产品类别
	 */
	private String productType;
	/**
	 * 库位信息
	 */
	private String place;
	/**
	 * 车辆状态
	 */
	private String truckStaus;
	/**
	 * 产品ID/产品分类ID
	 */
	private String sequenceId;
	
	/**
	 * 安检状态
	 */
	private String safeCheck;
	/**
	 * 产品名称
	 */
	private String productName;
	/**
	 * 计划装车量
	 */
	private String plannedCapacity;
	/**
	 * 库管分配量
	 */
	private String distributionAmount;
	public String getBillid() {
		return billid;
	}
	public void setBillid(String billid) {
		this.billid = billid;
	}
	public String getTruckNumber() {
		return truckNumber;
	}
	public void setTruckNumber(String truckNumber) {
		this.truckNumber = truckNumber;
	}
	public String getQueueid() {
		return queueid;
	}
	public void setQueueid(String queueid) {
		this.queueid = queueid;
	}
	public String getTruckCounts() {
		return truckCounts;
	}
	public void setTruckCounts(String truckCounts) {
		this.truckCounts = truckCounts;
	}
	public String getProductType() {
		return productType;
	}
	public void setProductType(String productType) {
		this.productType = productType;
	}
	public String getPlace() {
		return place;
	}
	public void setPlace(String place) {
		this.place = place;
	}
	public String getTruckStaus() {
		return truckStaus;
	}
	public void setTruckStaus(String truckStaus) {
		this.truckStaus = truckStaus;
	}
	public String getSequenceId() {
		return sequenceId;
	}
	public void setSequenceId(String sequenceId) {
		this.sequenceId = sequenceId;
	}
	public String getSafeCheck() {
		return safeCheck;
	}
	public void setSafeCheck(String safeCheck) {
		this.safeCheck = safeCheck;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public String getPlannedCapacity() {
		return plannedCapacity;
	}
	public void setPlannedCapacity(String plannedCapacity) {
		this.plannedCapacity = plannedCapacity;
	}
	public String getDistributionAmount() {
		return distributionAmount;
	}
	public void setDistributionAmount(String distributionAmount) {
		this.distributionAmount = distributionAmount;
	}
}
